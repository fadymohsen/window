<h3>عنوان الصفحة (Title Tag)</h3>

<p>العنوان الذي يظهر في نتائج جوجل. يجب أن يكون بين 50-60 حرفًا، يتضمن الكلمة المفتاحية الأساسية في البداية، ويكون جاذبًا بما يكفي لتحفيز النقر مثال: "أفضل شركة لوحات إعلانية في الرياض | وكالة ويندو".</p>

<h3>الوصف التعريفي (Meta Description)</h3>

<p>نص قصير (140-160 حرفًا) يظهر تحت العنوان في نتائج البحث. لا يؤثر مباشرة على الترتيب لكنه يؤثر بشكل كبير على معدل النقر (CTR). يجب أن يكون ملخصًا مغريًا للمقالة مع دعوة ضمنية للنقر.</p>

<h3>رابط الصفحة (URL/Slug)</h3>

<p>الرابط يجب أن يكون قصيرًا ووصفيًا وبالإنجليزية. تجنّب الروابط العربية المترجمة حرفيًا لأنها تظهر طويلة ومشوّهة في المتصفح. مثال جيد: <code>blogs/best-signage-company-riyadh/</code> بدلًا من <code>blogs/afdl-shrk-lohat-aalany-fy-alryad</code>.</p>

<h3>الروابط الداخلية والخارجية</h3>

<p>الروابط الداخلية تربط مقالتك بصفحات أخرى في موقعك، مما يساعد جوجل على فهم بنية الموقع وتوزيع قوة الصفحة (Page Authority). اربط بـ 3-5 صفحات داخلية ذات صلة في كل مقالة. الروابط الخارجية لمصادر موثوقة تزيد مصداقية محتواك.</p>

