<h3>Q: What's the difference between a visual identity and a logo?</h3>

<p>A logo is one element within the larger visual identity system. A complete visual identity includes: the logo in its various versions, a color palette (primary, secondary, accent), primary and secondary typography, graphic elements and icons, photography style, application templates for print and digital, and a brand guidelines document that defines the rules. Treating a logo alone as the "identity" is a common mistake that leads to inconsistent designs with no brand signature.</p>

